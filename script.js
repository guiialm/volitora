function clickMenu() {
if (nav.itens.style.display == 'block') {
    nav.itens.style.display == 'none'
} else {
    itens.display.style == 'block'
}
}

function mudouTamanho() {
    if (window.innerWidth >= 768) {
        nav.itens.style.display = 'block'
    } else {
        nav.itens.style.display = 'none'
    }
}
